# Load the dataset into a pandas dataframe.
train_df = pd.read_csv("d:/data_file/sentiment-data/train_3w.txt", delimiter='\t', header=None,
                       names=['num_id', 'label', 'sentence'])
test_df = pd.read_csv("d:/data_file/sentiment-data/test_5k.txt", delimiter='\t', header=None,
                      names=['num_id', 'label', 'sentence'])

# Report the number of sentences.
print('Number of test sentences: {:,}\n'.format(train_df.shape[0]))

# Create sentence and label lists
train_sentences = train_df.sentence.values
train_labels = train_df[["label"]].applymap(lambda x: lab_map.get(x)).values

test_sentences = test_df.sentence.values
test_labels = test_df[["label"]].applymap(lambda x: lab_map.get(x)).values

# Tokenize all of the sentences and map the tokens to thier word IDs.
train_input_ids, test_input_ids = [], []
train_attention_masks, test_attention_masks = [], []

# For every sentence...
for sent in train_sentences:
    # `encode_plus` will:
    #   (1) Tokenize the sentence.
    #   (2) Prepend the `[CLS]` token to the start.
    #   (3) Append the `[SEP]` token to the end.
    #   (4) Map tokens to their IDs.
    #   (5) Pad or truncate the sentence to `max_length`
    #   (6) Create attention masks for [PAD] tokens.
    encoded_dict = tokenizer.encode_plus(
        sent,  # Sentence to encode.
        add_special_tokens=True,  # Add '[CLS]' and '[SEP]'
        max_length=100,  # Pad & truncate all sentences.
        pad_to_max_length=True,
        return_attention_mask=True,  # Construct attn. masks.
        return_tensors='pt',  # Return pytorch tensors.
    )

    # Add the encoded sentence to the list.
    train_input_ids.append(encoded_dict['input_ids'])

    # And its attention mask (simply differentiates padding from non-padding).
    train_attention_masks.append(encoded_dict['attention_mask'])

for sent in test_sentences:
    encoded_dict = tokenizer.encode_plus(
        sent,  # Sentence to encode.
        add_special_tokens=True,  # Add '[CLS]' and '[SEP]'
        max_length=100,  # Pad & truncate all sentences.
        pad_to_max_length=True,
        return_attention_mask=True,  # Construct attn. masks.
        return_tensors='pt',  # Return pytorch tensors.
    )
    test_input_ids.append(encoded_dict['input_ids'])
    test_attention_masks.append(encoded_dict['attention_mask'])


# Convert the lists into tensors.
train_input_ids = torch.cat(train_input_ids, dim=0)
train_attention_masks = torch.cat(train_attention_masks, dim=0)
train_labels = torch.tensor(train_labels)

test_input_ids = torch.cat(test_input_ids, dim=0)
